import { z } from "zod";
import { writeFileSync, mkdirSync, readFileSync, existsSync } from "fs";
import { join } from "path";
import type { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import type { Config, ImageProvider } from "../types.js";
import { generateHtmlPage, escapeHtml, safeJsonForScript } from "./templates.js";
import { createImageProvider } from "./image-generator.js";

export const AddShopInput = {
  siteDir: z.string().describe("Absolute path to existing site directory"),
  products: z
    .array(
      z.object({
        name: z.string(),
        price: z.number().describe("Price in dollars"),
        description: z.string().optional(),
        tags: z.array(z.string()).optional(),
      })
    )
    .describe("Products to add to the shop"),
  currency: z.string().optional().default("USD").describe("Currency code (e.g. USD, AUD)"),
};

type Product = {
  name: string;
  price: number;
  description?: string;
  tags?: string[];
};

type AddShopInputType = {
  siteDir: string;
  products: Product[];
  currency?: string;
};

export interface AddShopResult {
  files: string[];
  siteDir: string;
  imagesGenerated: number;
  imageProvider: string;
  estimatedImageCost: string;
}

function slugify(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

