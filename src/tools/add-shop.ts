  // --- workers/shop-api/index.js ---
  const workerJs = `/* workers/shop-api/index.js — generated by web-builder-mcp */
/* Deploy with: wrangler deploy */

const CORS_HEADERS = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Methods': 'GET, POST, OPTIONS',
  'Access-Control-Allow-Headers': 'Content-Type',
};

function json(data, status = 200) {
  return new Response(JSON.stringify(data), {
    status,
    headers: { 'Content-Type': 'application/json', ...CORS_HEADERS },
  });
}

function corsResponse() {
  return new Response(null, { status: 204, headers: CORS_HEADERS });
}

export default {
  async fetch(request, env) {
    const url = new URL(request.url);

    if (request.method === 'OPTIONS') return corsResponse();

    if (url.pathname === '/health') {
      return json({ status: 'ok', timestamp: new Date().toISOString() });
    }

    if (url.pathname === '/create-checkout' && request.method === 'POST') {
      try {
        const body = await request.json();
        const { items } = body;

        if (!items || !Array.isArray(items) || items.length === 0) {
          return json({ error: 'No items in cart' }, 400);
        }

        // Calculate total in cents
        const totalCents = items.reduce((sum, item) => {
          const price = Math.round(item.price * 100);
          const qty = parseInt(item.qty, 10) || 1;
          return sum + price * qty;
        }, 0);

        // TODO: Integrate with Stripe or preferred payment provider
        // Example: const session = await stripe.checkout.sessions.create({ ... });
        // return json({ url: session.url });

        // Placeholder response
        return json({
          ok: true,
          totalCents,
          message: 'Checkout integration not yet configured. Set STRIPE_SECRET_KEY in wrangler.toml secrets.',
        });
      } catch (err) {
        return json({ error: 'Invalid request body' }, 400);
      }
    }

    if (url.pathname === '/webhook' && request.method === 'POST') {
      // TODO: Verify webhook signature and process payment events
      return json({ received: true });
    }

    return new Response('Not found', { status: 404 });
  },
};
`;
  writeFileSync(join(siteDir, "workers", "shop-api", "index.js"), workerJs, "utf-8");
