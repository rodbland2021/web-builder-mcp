export function generateSiteJs(): string {
  return `/* Generated by web-builder-mcp — site.js */
(function () {
  'use strict';

  // Mobile nav toggle
  const navToggle = document.querySelector('.nav-toggle');
  const navLinks = document.querySelector('.nav-links');
  const navOverlay = document.querySelector('.nav-overlay');

  function openNav() {
    if (!navToggle || !navLinks) return;
    navLinks.classList.add('is-open');
    navToggle.setAttribute('aria-expanded', 'true');
    if (navOverlay) navOverlay.classList.add('is-open');
    document.body.style.overflow = 'hidden';
  }

  function closeNav() {
    if (!navToggle || !navLinks) return;
    navLinks.classList.remove('is-open');
    navToggle.setAttribute('aria-expanded', 'false');
    if (navOverlay) navOverlay.classList.remove('is-open');
    document.body.style.overflow = '';
  }

  if (navToggle) {
    navToggle.addEventListener('click', function () {
      const isOpen = navToggle.getAttribute('aria-expanded') === 'true';
      if (isOpen) { closeNav(); } else { openNav(); }
    });
  }

  // Overlay click-to-close
  if (navOverlay) {
    navOverlay.addEventListener('click', closeNav);
  }

  // Escape key handler for all overlays
  document.addEventListener('keydown', function (e) {
    if (e.key === 'Escape') {
      closeNav();
      // Close any other open overlays
      document.querySelectorAll('.overlay.is-open, .drawer.is-open').forEach(function (el) {
        el.classList.remove('is-open');
      });
    }
  });

  // Dynamic copyright year
  const yearEls = document.querySelectorAll('.copyright-year');
  yearEls.forEach(function (el) {
    el.textContent = new Date().getFullYear().toString();
  });

})();
`;
}

export function generateHtmlPage(opts: HtmlPageOpts): string {
  const lang = opts.lang ?? "en-US";
  const description = escapeHtml(opts.description ?? "");
  const safeTitle = escapeHtml(opts.title);
  const businessName = opts.businessName ?? safeTitle;
  const cssFile = opts.cssFile ?? "styles.css";
  const jsFile = opts.jsFile ?? "site.js";
  const extraCss = opts.extraCss ?? [];
  const extraJs = opts.extraJs ?? [];

  const defaultNavLinks = [
    { href: "index.html", label: "Home" },
    { href: "about.html", label: "About" },
    { href: "contact.html", label: "Contact" },
  ];
  const navLinks = opts.navLinks ?? defaultNavLinks;

  const navItems = navLinks.map((l) => {
    const isActive = opts.currentPage && l.href === opts.currentPage;
    const activeAttr = isActive ? ` class="active" aria-current="page"` : "";
    return `        <li><a href="${escapeHtml(l.href)}"${activeAttr}>${escapeHtml(l.label)}</a></li>`;
  }).join("\n");
  const extraCssTags = extraCss.map((f) => `  <link rel="stylesheet" href="${f}">`).join("\n");
  const extraJsTags = extraJs.map((f) => `  <script src="${f}" defer></script>`).join("\n");

  const canonicalTag = opts.canonicalUrl
    ? `  <link rel="canonical" href="${opts.canonicalUrl}">\n`
    : "";

  // Phone number in nav
  const phoneHtml = opts.phone
    ? `\n      <a href="tel:${escapeHtml(opts.phone.replace(/\s/g, ""))}" class="nav-phone">${escapeHtml(opts.phone)}</a>`
    : "";

  // CTA button in nav
  const ctaHtml = opts.ctaButton
    ? `\n      <a href="${escapeHtml(opts.ctaButton.href)}" class="nav-cta">${escapeHtml(opts.ctaButton.text)}</a>`
    : "";

  // Footer content
  let footerHtml: string;
  if (opts.footerContent) {
    const attribution = opts.unsplashAttribution
      ? `\n      <p class="footer-bottom">${opts.unsplashAttribution}</p>`
      : "";
    footerHtml = `  <footer class="site-footer">
    <div class="container">
${opts.footerContent}
      <div class="footer-bottom">
        <p>&copy; <span class="copyright-year"></span> ${businessName}. All rights reserved.</p>
      </div>${attribution}
    </div>
  </footer>`;
  } else {
    const attribution = opts.unsplashAttribution
      ? `\n      <p>${opts.unsplashAttribution}</p>`
      : "";
    footerHtml = `  <footer class="site-footer">
    <div class="container">
      <p>&copy; <span class="copyright-year"></span> ${businessName}. All rights reserved.</p>${attribution}
    </div>
  </footer>`;
  }

  return `<!DOCTYPE html>
<html lang="${lang}">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <meta name="description" content="${description}">
  <meta property="og:title" content="${safeTitle}">
  <meta property="og:description" content="${description}">
  <meta property="og:type" content="website">${opts.ogImage ? `\n  <meta property="og:image" content="${escapeHtml(opts.ogImage)}">` : ""}
  <title>${safeTitle}</title>
${canonicalTag}  <link rel="stylesheet" href="${cssFile}">
${extraCssTags ? extraCssTags + "\n" : ""}${opts.extraHead ?? ""}
</head>
<body>
  <a href="#main" class="skip-link">Skip to content</a>

  <div class="nav-overlay" aria-hidden="true"></div>

  <header class="site-header">
    <div class="container nav-inner">
      <a href="index.html" class="nav-logo">${businessName}</a>${phoneHtml}
      <button class="nav-toggle" aria-expanded="false" aria-label="Toggle navigation">
        <span></span>
        <span></span>
        <span></span>
      </button>
      <nav aria-label="Main navigation">
        <ul class="nav-links">
${navItems}
        </ul>
      </nav>${ctaHtml}
    </div>
  </header>

  <main id="main">
${opts.bodyContent}
  </main>

${footerHtml}

  <script src="${jsFile}" defer></script>
${extraJsTags ? extraJsTags + "\n" : ""}${opts.ldJson ? `  <script type="application/ld+json">\n${opts.ldJson}\n  </script>\n` : ""}</body>
</html>`;
}
