export function registerAddBooking(server: McpServer, config: Config): void {
  const provider = createImageProvider(config);
  server.registerTool(
    "add_booking",
    {
      description:
        "Add a multi-step booking system to an existing site: service selection, date/time picker, details form, AI-generated hero image, and a Cloudflare Worker for notifications",
      inputSchema: AddBookingInput,
    },
    async (args) => {
      const result = await addBooking(args as AddBookingInputType, { imageProvider: provider });
      return {
        content: [
          {
            type: "text" as const,
            text: JSON.stringify(result, null, 2),
          },
        ],
      };
    }
  );
}
