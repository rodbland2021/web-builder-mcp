export const AddBookingInput = {
  siteDir: z.string().describe("Absolute path to existing site directory"),
  services: z.array(z.string()).describe("List of bookable services (e.g. ['Haircut', 'Massage'])"),
  businessName: z.string().optional().describe("Business name for the booking page header"),
};

type AddBookingInputType = {
  siteDir: string;
  services: string[];
  businessName?: string;
};

export interface AddBookingResult {
  files: string[];
  siteDir: string;
  imagesGenerated: number;
}

